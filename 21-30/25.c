#include<stdio.h>
int main()
{
  int x = 1;
  int y = 2;
  int z = 3;

  if(x==y)
      printf("x == y \n");
  if(x != y)
      printf("x != y \n");
  if(x > y)
      printf("x > y \n");
  if(x < y)
      printf("x < y \n");
  if(y >= z)
      printf("y >= z \n");
  if(y <= z)
      printf("y <= z \n");
}
